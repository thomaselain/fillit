#include "../includes/fillit.h"

void		c_pieces(t_env *e)
{
	int		c;
	int		d;
	int		i;

	c = 0;
	d = 0;
	i = 0;
	while (e->buf[i] != '\0')
	{
		if (e->buf[i] == '#')
			c++;
		else if (e->buf[i] == '.')
			d++;
		i++;
	}
	if (c % 4 == 0 && d % 12 == 0 && d > 0)
		e->nb_piece = (c / 4);
}
